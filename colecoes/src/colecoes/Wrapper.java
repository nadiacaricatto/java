package colecoes;

public class Wrapper {

	public static void main(String[] args) {
		
		int numero; //variável primitiva que guarda um valor
		
		Integer idade; //Integer com I maiúsculo é um objeto que encapsula (guarda) um número
		//aqui, ela transforma um primitivo em objeto para poder ser trabalhado em uma classe/collection
		//collections são obrigatoriamente objetos 
		
		System.out.println("numero");
		System.out.println("idade");
		
		
	}

}
